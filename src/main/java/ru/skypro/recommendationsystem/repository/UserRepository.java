package ru.skypro.recommendationsystem.repository;

import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.stereotype.Repository;
import ru.skypro.recommendationsystem.entity.User;

import java.util.List;
import java.util.UUID;

/**
 * Репозиторий для работы с сущностью {@link User}.
 * <p>
 * Расширяет стандартный {@code JpaRepository}, предоставляя базовые операции CRUD
 * (сохранение, поиск по ID, удаление и т.д.) для сущности {@code User} с ключом {@code UUID}.
 * </p>
 *
 * <h3>Ключевой метод: Поиск по имени пользователя</h3>
 * <p>
 * Метод {@link #findByUsername(String)} реализует поиск пользователей по публичному
 * идентификатору ({@code username}). Это необходимо для сценариев:
 * </p>
 * <ul>
 *     <li>Аутентификации (проверка существования логина при входе).</li>
 *     <li>Администрирования (поиск пользователя в админ-панели по нику).</li>
 *     <li>Интеграции с внешними системами, где известен только логин.</li>
 * </ul>
 *
 * <h4>Важное замечание о сигнатуре метода:</h4>
 * <p>
 * Метод возвращает {@code List<User>}, а не {@code Optional<User>}. Это сделано намеренно
 * по двум причинам:
 * </p>
 * <ol>
 *     <li><b>Отсутствие гарантии уникальности на уровне кода:</b> Хотя в сущности {@code User}
 *         поле {@code username} помечено как {@code unique = true}, в некоторых сценариях
 *         миграции БД или тестовых данных это ограничение может быть временно отключено.
 *         Возврат списка позволяет обработать ситуацию "дубликатов" явно, а не падать с ошибкой.</li>
 *
 *     <li><b>Гибкость поиска:</b> В будущем бизнес-логика может потребовать поиска по частичному
 *         совпадению имени (префикс), и сигнатура метода останется совместимой (просто изменится
 *         реализация запроса).</li>
 * </ol>
 *
 * <strong>Рекомендация для использования:</strong> В сервисном слое всегда предполагайте, что
 * результат может содержать 0, 1 или более элементов. Для сценариев аутентификации ожидается
 * ровно один элемент. Если найдено больше одного — это критическая ошибка целостности данных,
 * которую нужно логировать и обрабатывать.
 * </p>
 *
 * @see User
 */
@Repository
public interface UserRepository extends JpaRepository<User, UUID> {

    /**
     * Находит пользователей по их публичному имени (логину).
     * <p>
     * Выполняет SQL-запрос вида: {@code SELECT * FROM users WHERE username = ?}.
     * </p>
     *
     * <h4>Ожидаемые сценарии результатов:</h4>
     * <dl>
     *     <dt>0 элементов</dt>
     *         <dd>Пользователь с таким логином не найден. В контексте аутентификации —
     *             неверный логин.</dd>
     *
     *     <dt>1 элемент</dt>
     *         <dd>Успешный поиск. Стандартный сценарий для входа в систему.</dd>
     *
     *     <dt>&gt;1 элемента</dt>
     *         <dd><b>Критическая ситуация.</b> Нарушение бизнес-правила уникальности логина.
     *             В продакшене такого быть не должно. Сервисный слой обязан перехватить
     *             этот случай, залогировать ошибку и вернуть пользователю общую ошибку
     *             "Внутренняя ошибка системы", не раскрывая деталей.</dd>
     * </dl>
     *
     * @param username публичное имя пользователя (логин), чувствительность к регистру зависит
     *                 от настроек базы данных (обычно case-insensitive в PostgreSQL при использовании
     *                 ILIKE, но здесь используется точное совпадение =).
     * @return список пользователей с указанным именем (обычно 0 или 1 элемент)
     */
    List<User> findByUsername(String username);
}
