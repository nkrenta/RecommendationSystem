package ru.skypro.recommendationsystem.service;

import ru.skypro.recommendationsystem.DTO.RecommendationDTO;

import java.util.List;
import java.util.UUID;

/**
 * Контракт (API) для движка правил генерации рекомендаций.
 * <p>
 * Этот интерфейс определяет единый способ получения списка рекомендаций для конкретного пользователя.
 * Реализация этого интерфейса может варьироваться:
 * </p>
 *
 * <h3>Семантика метода {@link #checkRecommendation(UUID)}:</h3>
 * <p>
 * Метод принимает идентификатор пользователя и возвращает список подходящих рекомендаций.
 * </p>
 *
 * <h4>Важные аспекты поведения (реализация-зависимые):</h4>
 * <dl>
 *     <dt>Побочные эффекты (Side Effects)</dt>
 *         <dd>Контракт <b>не запрещает</b> побочные эффекты внутри реализации.
 *             Например, конкретная реализация ({@code DynamicRuleSet}) может обновлять
 *             счетчики статистики срабатывания правил в рамках этой операции.
 *             Вызывающий код должен предполагать, что состояние системы может измениться.</dd>
 *
 *     <dt>Пустой список</dt>
 *         <dd>Если для пользователя не найдено ни одной подходящей рекомендации, метод
 *             обязан вернуть пустой список ({@code Collections.emptyList()} или новый {@code ArrayList}),
 *             но <b>никогда не должен возвращать {@code null}</b>.</dd>
 *
 *     <dt>Порядок элементов</dt>
 *         <dd>Порядок рекомендаций в списке имеет значение. Реализация должна возвращать
 *             рекомендации в порядке убывания приоритета (или релевантности).
 *             Если приоритеты равны, порядок может быть произвольным.</dd>
 * </dl>
 *
 * @see RecommendationDTO
 */
public interface RecommendationRuleSet {

    /**
     * Генерирует список рекомендаций для указанного пользователя.
     * <p>
     * Выполняет проверку всех активных правил (или источников рекомендаций) против
     * профиля и транзакционной истории пользователя.
     * </p>
     *
     * <h4>Логика работы (на примере реализации через динамические правила):</h4>
     * <ol>
     *     <li>Загрузка набора правил.</li>
     *     <li>Проверка условий каждого правила для данного пользователя.</li>
     *     <li>(Опционально) Обновление внутренней статистики (счетчиков срабатываний).</li>
     *     <li>Формирование объектов {@link RecommendationDTO} для сработавших правил.</li>
     *     <li>Возврат отсортированного списка.</li>
     * </ol>
     *
     * @param userId идентификатор пользователя, для которого требуется сформировать рекомендации
     * @return список объектов {@link RecommendationDTO}. Гарантированно не {@code null}.
     *         Может быть пустым, если подходящих рекомендаций нет.
     */
    List<RecommendationDTO> checkRecommendation(UUID userId);
}
